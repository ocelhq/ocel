import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

// Pre-bundle each framework's handler shim (adapter logic + its npm deps, e.g.
// serverless-http) into a self-contained string at BUILD time, so the runtime
// builder never needs a bundler. The entrypoint path is left as a placeholder
// the runtime substitutes per app.
const dir = path.dirname(fileURLToPath(import.meta.url));
const pkg = path.resolve(dir, "..");

const ENTRY_PLACEHOLDER = "__OCEL_ENTRY_PLACEHOLDER__";
const banner =
  "import { createRequire as __nbCreateRequire } from 'node:module';\n" +
  "const require = __nbCreateRequire(import.meta.url);";

const result = await build({
  entryPoints: [path.join(pkg, "src/shim/express.entry.ts")],
  bundle: true,
  format: "esm",
  platform: "node",
  target: "node20",
  write: false,
  banner: { js: banner },
  define: { __OCEL_ENTRY__: JSON.stringify(ENTRY_PLACEHOLDER) },
});

const text = result.outputFiles[0].text;
const out = `// GENERATED by scripts/gen-shim.mjs — do not edit.\nexport default ${JSON.stringify(text)};\n`;
await writeFile(path.join(pkg, "src/shim/express.bundled.ts"), out);

console.log("generated src/shim/express.bundled.ts");
